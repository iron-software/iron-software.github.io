function show_helpscout(e) {

	let widget_status = HubSpotConversations.widget.status();

	if (widget_status.loaded) {
		window.HubSpotConversations.widget.close()
	} else {
		window.HubSpotConversations.widget.open()
	}


	if (e) {
		e.preventDefault()
		e.stopPropagation();
	}
	return false
}


